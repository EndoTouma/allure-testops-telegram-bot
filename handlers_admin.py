import logging
import os
import re

from telegram import Update
from telegram.ext import ContextTypes

from db import add_allowed_user, remove_allowed_user, list_allowed_users

logger = logging.getLogger(__name__)

# –ó–∞–≥—Ä—É–∂–∞–µ–º —Å–ø–∏—Å–æ–∫ –≤–ª–∞–¥–µ–ª—å—Ü–µ–≤ –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –æ–∫—Ä—É–∂–µ–Ω–∏—è
raw = os.getenv("OWNER_USERNAMES", "")
OWNER_USERNAMES = {u.strip() for u in raw.split(",") if u.strip()}


async def allow_user(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    /allow_user <telegram_username>
    –î–æ–±–∞–≤–ª—è–µ—Ç —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–ø–æ @username) –≤ –±–µ–ª—ã–π —Å–ø–∏—Å–æ–∫.
    –î–æ—Å—Ç—É–ø–Ω–∞ —Ç–æ–ª—å–∫–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º –∏–∑ OWNER_USERNAMES.
    """
    from_user = update.effective_user.username
    if from_user not in OWNER_USERNAMES:
        return

    if not context.args:
        return await update.message.reply_text("–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: /allow_user <telegram_username>")

    raw_username = context.args[0].lstrip("@")
    if not re.fullmatch(r"[A-Za-z0-9_]+", raw_username):
        return await update.message.reply_text(
            "–ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç username, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Ç–æ–ª—å–∫–æ –±—É–∫–≤—ã, —Ü–∏—Ñ—Ä—ã –∏ –ø–æ–¥—á—ë—Ä–∫–∏–≤–∞–Ω–∏—è."
        )

    try:
        add_allowed_user(raw_username)
    except Exception as e:
        logger.error(f"–ù–µ —É–¥–∞–ª–æ—Å—å –¥–æ–±–∞–≤–∏—Ç—å –≤ allowed_users: {e}")
        return await update.message.reply_text("‚ùó –û—à–∏–±–∫–∞ –ø—Ä–∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.")

    await update.message.reply_text(f"‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å @{raw_username} –¥–æ–±–∞–≤–ª–µ–Ω –≤ –±–µ–ª—ã–π —Å–ø–∏—Å–æ–∫.")


